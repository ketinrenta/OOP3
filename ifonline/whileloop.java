public class whileloop {
    public static void main(String[] args){
        
        System.out.println("perulangan dengan while");
        int AC = 0;
        int batas = 10;
        
        while(AC < batas){
            System.out.println("perulangan ke-"+AC);
            AC = AC+2;//naikin 2 AC =AC+2 AC++
        }
    }
    
}
